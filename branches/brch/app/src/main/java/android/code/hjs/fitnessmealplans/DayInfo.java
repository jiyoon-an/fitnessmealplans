public class DayInfo {

    private String date;
    private boolean bMonth;

    public void setDate(String date) {
        this.date = date;
    }

    public void setbMonth(boolean bMonth) {
        this.bMonth = bMonth;
    }

    public String getDate() {
        return date;
    }

    public boolean isbMonth() {
        return bMonth;
    }
}
